<button class="accordion">HOW TO BEST DEAL WITH FEAR</button>
<div class="panel">

<p class="mytext">
Fear, as well as faith, is a spirit, do not get all spooky with this because they have been working in your life continuously - a combination of the things you have either resisted or the things you've allowed. Sadly you may have allowed fear to rule you until it's now difficult to differentiate when fear attacks you.
</p>
<p class="mytext">
I personally have heard and also experienced instances where I was so scared to the point that I became traumatized and could not do or say anything. After the incident was over, I asked myself why I acted that way.
</p>
<p class="mytext">
I have seen fear drive people into doing some of the stupidest and dumbest things in life - because fear takes over and leads you in the wrong direction all the time.
</p>
<p class="mytext">
Be assured that every man or woman who has accomplished any great thing on earth has had to confront some kind of fear, so you really don't have to be afraid; once you take your stand, fear will have to give way.
</p>

<p class="mytext">
The way I have been dealing with fear is to first I typically go through every area of my life ask myself these questions - what is it that I am afraid or scared of? What is it that I'm afraid of losing or Would it work out?.
 I keep digging until I find out what it is I'm really afraid of. The outcome for me is always the fear of loss - some irrecoverable loss.
Remember you have to search until you have found what exactly you're afraid of before you can go to the next step.

</p>
<p class="mytext">
Secondly, now that you have identified what it is that you are afraid of then resist it. There is something very wonderful about your mouths (the tongue) because in it lies the ability to change the course of situations either in you or around you. Unfortunately many have used it against themselves more often than for themselves.
</p>
<p class="mytext">
History has shown us that with your mouth we can make or break you including lives (THESE WORDS CAN SHAPE YOUR FUTURE - GOOD or BAD, <a href="http://www.aniekanjoshuawhyte.com/?p=1269">More</a>) - words especially your words have tremendous influence over your life.
</p>
<p class="mytext">
Resisting fear starts with your mouth, you say it to yourself - like a mutter under your breathe. 'I refuse to fear', those four words have been a lifesaver for me. The more prominent the fear the louder you get into saying it... I refuse to fear. I mean even with a full blown panic attack where you're actually shaking and all scared is when you let your mouth go to work for you. You keep speaking and saying it out loud "I refuse to fear" "fear I resist you, go from me" "I refuse to fear".
</p>
<p class="mytext">
Fear is a persistent thing and you have to be more persistent to subdue and stay on top of it. Now the good thing is that if you stand your ground and do not give in or give up it leaves you but for a while.
</p>
<p class="mytext">
Anytime you have feelings of fear, thoughts of fear, something is trying to shake you or move you don't just be silent, speak up against it and if in a crowded place, go someplace secluded. Say out loud fear, I resist you (it's a spirit).
